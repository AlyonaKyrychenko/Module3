namespace ConsoleApp2
{
    /// <summary>
    /// This is class for summing two numbers.
    /// </summary>
    public static class Summator
    {
        /// <summary>
        /// This is method for summing two numbers.
        /// </summary>
        /// <param name="firstNumber">First number to sum.</param>
        /// <param name="secondNumber">Second numder to sum.</param>
        /// <returns>Result of summing two numbers.</returns>
        public static int CalcSum(int firstNumber, int secondNumber)
        {
            return firstNumber + secondNumber;
        }
    }
}
